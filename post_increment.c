#include<stdio.h>

int main()
{
int x;
x = 5;

printf(" Enter any value : %d \n", x);

printf(" Increment value : %d \n", x++); // x++ means here first use,then increment 

printf(" Final answer after increment : %d \n", x);

return 0;
}

// output is --
// Enter any value : 5
// Incremetn value : 5
// Final answer after increment : 6
